// Função para obter todos os produtos com filtros opcionais
export async function getProdutos({
  categoria,
  clube,
  cor,
  liga,
  ordenar = "recentes",
  precoMin,
  precoMax,
  temporada,
  edicao_especial,
  pesquisa,
}: {
  categoria?: string
  clube?: string
  cor?: string
  liga?: string
  ordenar?: string
  precoMin?: string
  precoMax?: string
  temporada?: string
  edicao_especial?: boolean
  pesquisa?: string
}) {
  // Simular um atraso de rede
  await new Promise((resolve) => setTimeout(resolve, 500))

  let produtosFiltrados = [...produtos]

  // Filtrar por pesquisa de texto
  if (pesquisa && pesquisa.trim()) {
    const termoPesquisa = pesquisa.toLowerCase().trim()
    produtosFiltrados = produtosFiltrados.filter((produto) => 
      produto.nome.toLowerCase().includes(termoPesquisa) ||
      produto.descricao.toLowerCase().includes(termoPesquisa) ||
      produto.clube?.toLowerCase().includes(termoPesquisa) ||
      produto.liga?.toLowerCase().includes(termoPesquisa) ||
      produto.temporada?.toLowerCase().includes(termoPesquisa) ||
      produto.cor?.toLowerCase().includes(termoPesquisa) ||
      produto.marca?.toLowerCase().includes(termoPesquisa)
    )
  }

  if (categoria) {
    produtosFiltrados = produtosFiltrados.filter((produto) => produto.categoria === categoria)
  }

  if (clube) {
    produtosFiltrados = produtosFiltrados.filter((produto) => produto.clube === clube)
  }

  if (cor) {
    produtosFiltrados = produtosFiltrados.filter((produto) => produto.cor === cor)
  }

  if (liga) {
    if (liga === "outras-ligas") {
      const ligasPrincipais = ["liga-portuguesa", "premier-league", "la-liga", "serie-a", "bundesliga", "ligue-1"]
      produtosFiltrados = produtosFiltrados.filter((produto) => produto.liga && !ligasPrincipais.includes(produto.liga))
    } else {
      produtosFiltrados = produtosFiltrados.filter((produto) => produto.liga === liga)
    }
  }

  if (temporada) {
    produtosFiltrados = produtosFiltrados.filter((produto) => produto.temporada === temporada)
  }

  if (edicao_especial !== undefined) {
    produtosFiltrados = produtosFiltrados.filter((produto) => produto.edicao_especial === edicao_especial)
  }

  if (precoMin) {
    produtosFiltrados = produtosFiltrados.filter((produto) => produto.preco >= Number(precoMin))
  }

  if (precoMax) {
    produtosFiltrados = produtosFiltrados.filter((produto) => produto.preco <= Number(precoMax))
  }

  // Aplicar ordenação
  switch (ordenar) {
    case "preco-asc":
      produtosFiltrados.sort((a, b) => a.preco - b.preco)
      break
    case "preco-desc":
      produtosFiltrados.sort((a, b) => b.preco - a.preco)
      break
    case "populares":
      produtosFiltrados.sort((a, b) => b.numAvaliacoes - a.numAvaliacoes)
      break
    case "avaliacao":
      produtosFiltrados.sort((a, b) => b.avaliacao - a.avaliacao)
      break
    case "recentes":
    default:
      // Organizar por temporada primeiro (2025/26 em primeiro), depois manter ordem original
      produtosFiltrados.sort((a, b) => {
        // Primeiro critério: temporada (2025/26 primeiro)
        if (a.temporada === "2025/26" && b.temporada !== "2025/26") return -1
        if (a.temporada !== "2025/26" && b.temporada === "2025/26") return 1
        
        // Se ambas têm a mesma prioridade de temporada, manter ordem original
        return 0
      })
      break
  }

  return produtosFiltrados
}

// Função para obter produtos em destaque
export async function getDestaques() {
  // Simular um atraso de rede
  await new Promise((resolve) => setTimeout(resolve, 300))

  // Filtrar produtos em destaque
  const produtosDestaque = produtos.filter((p) => p.destaque)

  // Separar as camisolas do Real Madrid 2025/26
  const realMadridCamisolas = produtosDestaque.filter(
    (p) => p.id === "real-madrid-25-26-home" || p.id === "real-madrid-25-26-away"
  )

  // Separar a camisola do Sporting 2025/26
  const sportingCamisola = produtosDestaque.find((p) => p.id === "21")

  // Filtrar os outros produtos
  const outrosProdutos = produtosDestaque.filter(
    (p) => 
      p.id !== "21" && 
      p.id !== "real-madrid-25-26-home" && 
      p.id !== "real-madrid-25-26-away"
  )

  // Ordenar os produtos: Sporting, Real Madrid, e depois os outros
  const ordenedProducts = [
    ...(sportingCamisola ? [sportingCamisola] : []),
    ...realMadridCamisolas,
    ...outrosProdutos
  ]

  // Retornar todos os produtos em destaque (removida limitação de 8)
  return ordenedProducts
}

// Função para obter um produto pelo ID
export async function getProdutoById(id: string) {
  // Simular um atraso de rede
  await new Promise((resolve) => setTimeout(resolve, 300))

  return produtos.find((p) => p.id === id)
}

// Função para obter produtos relacionados
export async function getProdutosRelacionados(id: string, categoria: string) {
  // Simular um atraso de rede
  await new Promise((resolve) => setTimeout(resolve, 300))

  return produtos
    .filter((p) => p.id !== id && p.categoria === categoria)
    .sort(() => Math.random() - 0.5) // Ordenação aleatória
    .slice(0, 4)
}